var A = "Hello";

var B = "Hello";

// Equal (==)
console.log(A == B);

// Not Equal (!=)
console.log(A != B);

var num1 = 100;
var num2 = 200;

// Greater than (>)
console.log(num1 > num2);

// Less than (<)
console.log(num1 < num2);

// Greater than or Equal to (>=)
console.log(num1 >= num2);

// Less than or Equal to (<=)
console.log(num1 <= num2);