var A = 10;
var B = 20;

// Addition(+)
console.log(A + B);

// Addition operator works for Numeric as well as Strings
userName = "Bharath" + " " + "Reddy";
console.log(userName);

// Subtraction(-)
console.log(A - B);

// Multiplication(*)
console.log(A * B);

// Division(/)
console.log(A / B);

// Modulus(%)
console.log(A % B);

// Increment(++)
++A;
console.log(A);

// Decrement(--)
--B;
console.log(B);